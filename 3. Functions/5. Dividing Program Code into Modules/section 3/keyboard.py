###
# Functions to read any data type from the keyboard
#
def input_string(message: str):
    string = input(message)
    return string

def input_integer(message: str):
    integer = input(message)
    return integer

def input_real(message: str):
    real = input(message)
    return real

def input_boolean(message: str):
    boolean = input(message)
    return boolean